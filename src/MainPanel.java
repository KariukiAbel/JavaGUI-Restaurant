    private void jButton26ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton26ActionPerformed
        // TODO add your handling code here:
        String jibu;
        snum=Double.parseDouble(jTextField5.getText());
        if(op=="/"){
            result=fnum/snum;
            jibu=String.format("%.2f", result);
            jTextField5.setText(fnum + " / "+snum +" = "+jibu);
        }
         if(op=="*"){
            result=fnum*snum;
            jibu=String.format("%.2f", result);
            jTextField5.setText(fnum + " * "+snum +" = "+jibu);
        }
          if(op=="+"){
            result=fnum+snum;
            jibu=String.format("%.2f", result);
            jTextField5.setText(fnum + " + "+snum +" = "+jibu);
        }
           if(op=="-"){
            result=fnum-snum;
            jibu=String.format("%.2f", result);
            jTextField5.setText(fnum + " - "+snum +" = "+jibu);
        }
    }//GEN-LAST:event_jButton26ActionPerformed

